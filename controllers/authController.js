// @desc    Forgot password - Send reset token
// @route   POST /api/auth/forgot-password
// @access  Public
const forgotPassword = async (req, res) => {
  try {
    const { email } = req.body;

    if (!email) {
      return res.status(400).json({
        success: false,
        message: 'Email is required'
      });
    }

    const user = await User.findOne({ email });

    if (!user) {
      // Don't reveal if user exists or not for security
      return res.json({
        success: true,
        message: 'If an account exists with that email, you will receive password reset instructions.'
      });
    }

    // Generate reset token
    const resetToken = crypto.randomBytes(32).toString('hex');
    
    // Hash token and set to resetPasswordToken field
    const hashedToken = crypto
      .createHash('sha256')
      .update(resetToken)
      .digest('hex');

    // Set token and expiry (1 hour)
    user.resetPasswordToken = hashedToken;
    user.resetPasswordExpire = Date.now() + 60 * 60 * 1000; // 1 hour
    await user.save();

    // Create reset URL
    const resetUrl = `${process.env.FRONTEND_URL || 'http://localhost:3000'}/reset-password?token=${resetToken}`;

    console.log('\n' + '='.repeat(80));
    console.log('🔐 PASSWORD RESET REQUEST');
    console.log('='.repeat(80));
    console.log('📧 Email:', email);
    console.log('👤 User:', user.name);
    console.log('🔑 Reset Token:', resetToken);
    console.log('🔗 Reset URL:', resetUrl);
    console.log('⏰ Expires in: 1 hour');
    console.log('='.repeat(80) + '\n');

    // Send password reset email
    const emailResult = await sendPasswordResetEmail(user.email, resetUrl, user.name);
    
    if (emailResult.success) {
      console.log('✅ Password reset email sent successfully');
      if (emailResult.previewUrl) {
        console.log('📬 Preview Email:', emailResult.previewUrl);
      }
    } else {
      console.error('❌ Failed to send password reset email:', emailResult.message);
      // Continue anyway - don't reveal if email sending failed for security
    }
    
    res.json({
      success: true,
      message: 'If an account exists with that email, you will receive password reset instructions.',
      // In development only - include preview URL and token for testing
      data: process.env.NODE_ENV === 'development' ? {
        resetUrl,
        resetToken,
        emailPreviewUrl: emailResult.previewUrl || null
      } : undefined
    });
  } catch (error) {
    console.error('Forgot password error:', error);
    
    // Clear reset token fields if there was an error
    if (req.user) {
      req.user.resetPasswordToken = undefined;
      req.user.resetPasswordExpire = undefined;
      await req.user.save();
    }
    
    res.status(500).json({
      success: false,
      message: 'Error processing password reset request'
    });
  }
};

// @desc    Reset password using token
// @route   POST /api/auth/reset-password
// @access  Public
const resetPassword = async (req, res) => {
  try {
    const { token, newPassword } = req.body;

    if (!token || !newPassword) {
      return res.status(400).json({
        success: false,
        message: 'Token and new password are required'
      });
    }

    if (newPassword.length < 6) {
      return res.status(400).json({
        success: false,
        message: 'Password must be at least 6 characters long'
      });
    }

    // Get hashed token
    const hashedToken = crypto
      .createHash('sha256')
      .update(token)
      .digest('hex');

    // Find user by token and check if not expired
    const user = await User.findOne({
      resetPasswordToken: hashedToken,
      resetPasswordExpire: { $gt: Date.now() }
    });

    if (!user) {
      return res.status(400).json({
        success: false,
        message: 'Invalid or expired reset token'
      });
    }

    // Set new password
    user.password = newPassword;
    user.resetPasswordToken = undefined;
    user.resetPasswordExpire = undefined;
    await user.save();

    console.log(`Password reset successful for user: ${user.email}`);

    res.json({
      success: true,
      message: 'Password reset successful. You can now login with your new password.'
    });
  } catch (error) {
    console.error('Reset password error:', error);
    res.status(500).json({
      success: false,
      message: 'Error resetting password'
    });
  }
};

